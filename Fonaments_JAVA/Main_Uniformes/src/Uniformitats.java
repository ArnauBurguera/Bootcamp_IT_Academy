
public class Uniformitats {
	
	int id = 0;
	String nom = "";
	int unitats = 0;
	
	public Uniformitats(int id, String nom, int unitats) {
		this.id = id;
		this.nom = nom;
		this.unitats = unitats;
	}

	public int getId() {
		return this.id;
	}

	public String getNom() {
		return this.nom;
	}

	public int getUnitats() {
		return this.unitats;
	}

	public void setId(int id) {
		this.id = id;
	}

	public void setNom(String nom) {
		this.nom = nom;
	}

	public void setUnitats(int unitats) {
		this.unitats = unitats;
	}
	
	
	
	

}
